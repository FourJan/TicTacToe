import java.awt.Button;

public class TicTacToeButton extends Button {
	boolean isX;
	boolean isO;

	public boolean isX() {
		return isX;
	}

	public void setX(boolean isX) {
		this.isX = isX;
	}

	public boolean isO() {
		return isO;
	}

	public void setO(boolean isO) {
		this.isO = isO;
	}

}
